    private void btnNewReceiptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNewReceiptActionPerformed
       // String name = textReceiptName.getText( );
        String date = textReceiptDate.getText();
        String textAmmount = textReceiptAmmount.getText( );
        String type = textReceiptType.getText( );
        String textID = textReceiptID.getText();
        String details = textReceiptDescription.getText();
        int ID = Integer.parseInt(textID);
        double ammount = Double.parseDouble(textAmmount);
        
        handler = new DataEntryHandler();
        handler.addReceipt(ID, date, ammount, type, details);
        
        if(handler.getReceiptErrorMsg() == null)
            JOptionPane.showMessageDialog(rootPane, "Record added successfully");       
        else
            JOptionPane.showMessageDialog(rootPane, handler.getReceiptErrorMsg());
    }//GEN-LAST:event_btnNewReceiptActionPerformed

