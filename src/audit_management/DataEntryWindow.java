    private void btnNewPaymentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNewPaymentActionPerformed
       // String name = textPaymentName.getText( );
        String date = textPaymentDate.getText();
        String textAmmount = textPaymentAmmount.getText( );
        String type = textPaymentType.getText( );
        String textID = textPaymentID.getText();
        String details = textPaymentDescription.getText();
        int ID = Integer.parseInt(textID);
        double ammount = Double.parseDouble(textAmmount);
        
        handler = new DataEntryHandler();
        handler.addPayment(ID, date, ammount, type, details);
        
        if(handler.getPaymentErrorMsg() == null)
            JOptionPane.showMessageDialog(rootPane, "Record added successfully");       
        else
            JOptionPane.showMessageDialog(rootPane, handler.getPaymentErrorMsg());           
    }//GEN-LAST:event_btnNewPaymentActionPerformed

